package com.rmcet.alumni.model;

public class Alumini {
    String Afirname;
    String Amidname;
    String Alastname;
    String Pass;
    String Mobi;

    public Alumini() {
    }

    public String getAfirname() {
        return Afirname;
    }

    public void setAfirname(String afirname) {
        Afirname = afirname;
    }

    public String getAmidname() {
        return Amidname;
    }

    public void setAmidname(String amidname) {
        Amidname = amidname;
    }

    public String getAlastname() {
        return Alastname;
    }

    public void setAlastname(String alastname) {
        Alastname = alastname;
    }

    public String getPass() {
        return Pass;
    }

    public void setPass(String pass) {
        Pass = pass;
    }

    public String getMobi() {
        return Mobi;
    }

    public void setMobi(String mobi) {
        Mobi = mobi;
    }
}
